public class OrderType {
    private int type1,type2,type3;

    public OrderType() {
    }

    public OrderType(int type1, int type2, int type3) {
        this.type1 = type1;
        this.type2 = type2;
        this.type3 = type3;
    }

    public int getType1() {
        return type1;
    }

    public void setType1(int type1) {
        this.type1 = type1;
    }

    public int getType2() {
        return type2;
    }

    public void setType2(int type2) {
        this.type2 = type2;
    }

    public int getType3() {
        return type3;
    }

    public void setType3(int type3) {
        this.type3 = type3;
    }
    
    
}
