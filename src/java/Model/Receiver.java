package Model;

/**
 *
 * @author Anh Tuan
 */
public class Receiver {
    private int receiverID,userID;
    private String receiverName, email, mobile, gender,address, receiverType, createdAt, updatedAt;

    public Receiver() {
    }

    public Receiver(int receiverID, int userID, String receiverName, String email, String mobile, String gender, String address, String receiverType, String createdAt, String updatedAt) {
        this.receiverID = receiverID;
        this.userID = userID;
        this.receiverName = receiverName;
        this.email = email;
        this.mobile = mobile;
        this.gender = gender;
        this.address = address;
        this.receiverType = receiverType;
        this.createdAt = createdAt;
        this.updatedAt = updatedAt;
    }

    public int getReceiverID() {
        return receiverID;
    }

    public void setReceiverID(int receiverID) {
        this.receiverID = receiverID;
    }

    public int getUserID() {
        return userID;
    }

    public void setUserID(int userID) {
        this.userID = userID;
    }

    public String getReceiverName() {
        return receiverName;
    }

    public void setReceiverName(String receiverName) {
        this.receiverName = receiverName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getReceiverType() {
        return receiverType;
    }

    public void setReceiverType(String receiverType) {
        this.receiverType = receiverType;
    }

    public String getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(String createdAt) {
        this.createdAt = createdAt;
    }

    public String getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(String updatedAt) {
        this.updatedAt = updatedAt;
    }
    
    
}
