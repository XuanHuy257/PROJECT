    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
  String settingID = request.getParameter("sid");  // Get the setting ID from the request
        AdminDAO adminDAO = new AdminDAO();              // Create an instance of AdminDAO

        // Fetch the setting using the DAO method
        Settings setting = adminDAO.getSettingByID(settingID);

        // Check if the setting was found
        if (setting != null) {
            request.setAttribute("setting", setting);   // Set the setting object in the request
            request.getRequestDispatcher("admin-SettingDetail.jsp").forward(request, response);  // Forward to the JSP
        } else {
            // If the setting is not found, handle the error
            System.out.println("Setting not found for ID: " + settingID);  // Log the message
            response.sendError(HttpServletResponse.SC_NOT_FOUND, "Setting not found");  // Send a 404 error
        }
    }
    
    

